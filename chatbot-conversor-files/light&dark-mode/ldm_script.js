function modoClaro() {
    document.body.style.backgroundColor = 'rgb(255, 255, 255)';
    document.body.style.color = 'rgb(0, 0, 0)';  // Cor do texto no modo claro (preto)
}

function modoEscuro() {
    document.body.style.backgroundColor = 'rgb(25, 25, 25)';
    document.body.style.color = 'rgb(255, 255, 255)';  // Cor do texto no modo escuro (branco)
}